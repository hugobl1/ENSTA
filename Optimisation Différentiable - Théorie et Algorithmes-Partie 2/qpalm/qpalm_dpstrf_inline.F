      subroutine dpstrf2 (uplo, n, A, piv, rk, tol, work, info)

      integer i,j
      character uplo
      real*8  A(n,n)
      
      if ((uplo .eq. 'L') .or. (uplo .eq. 'l')) then
        do i=1,n
           do j=(i+1),n
              A(i,j)= 0.0
           enddo
        enddo
      else if ((uplo .eq. 'U') .or. (uplo .eq. 'u')) then
        do j=1,n
           do i=(j+1),n
              A(i,j)= 0.0
           enddo
       enddo
      endif

      call dpstrf (uplo, n, A, n, piv, rk, tol, work, info)

      return
      end

